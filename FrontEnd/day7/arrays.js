let fruits = ["apple", "banana", "orange"];
console.log(fruits);
console.log(fruits[0]);

// push()
fruits.push("mango");
console.log(fruits);

// pop()
fruits.pop();
console.log(fruits);

// shift() - remove the first element
fruits.shift();
console.log(fruits);

// unshift() - add an element at the beginning
fruits.unshift("kiwi");
console.log(fruits);